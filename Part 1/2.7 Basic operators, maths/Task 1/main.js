let a = 1, b = 1;

let c = ++a;  
// a = 2 because it has been incremented once from original value of 1
// c = 2 because the prefix increment operator returns the incremented value

let d = b++; 
// b = 2 because it has been incremented once from original value of 1
// d = 1 because the postfix increment operator returns the original value

